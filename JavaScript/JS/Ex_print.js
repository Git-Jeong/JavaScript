//JavaScript 출력문 
console.log("Hello World! console.log");
        
document.write("Hello World! document.write");

alert("hello World! alert");

//위의 출력문은 모두 Hello World!를 출력하는데, 각각의 출력문이 다른 방식으로 출력한다.
//우선순위 : alert > document.write > console.log
//alert는 경고창을 띄우는 방식으로 출력하고, document.write는 웹페이지에 출력하고, console.log는 개발자 도구 콘솔에 출력한다.